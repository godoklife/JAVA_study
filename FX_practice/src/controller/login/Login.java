package controller.login;

import java.net.URL;
import java.util.ResourceBundle;

import javafx.fxml.FXML;
import javafx.fxml.FXMLLoader;
import javafx.fxml.Initializable;
import javafx.scene.Parent;
import javafx.scene.layout.AnchorPane;
import javafx.scene.layout.BorderPane;
import javafx.scene.media.Media;
import javafx.scene.media.MediaPlayer;
import javafx.scene.media.MediaView;
import javafx.util.Duration;

public class Login implements Initializable	{

    @FXML
    private AnchorPane anchorpane;

    @FXML
    private MediaView mediaview;

    @FXML
    private BorderPane borderpane;

    
    
    @Override
    public void initialize(URL location, ResourceBundle resources) {
    // 1. 동영상 파일을 컨테이너에 넣기
    Media media = new Media(getClass().getResource("/img/thundermovie.mp4").toString());
    
    // 2. 미디어 플레이어 객체에 컨테이너를 인수로 전달
    MediaPlayer mediaPlayer = new MediaPlayer(media);
    
    // 3. fxml의 mediaview fx:id에 mediaPlayer 객체 설정
    mediaview.setMediaPlayer(mediaPlayer);
    
    // 4. 미디어플레이어 객체 시작
    mediaPlayer.play();
    
    // 5. 동영상 끝났을 떄 재반복
    mediaPlayer.setOnEndOfMedia(new Runnable() {
		@Override
		public void run() {
			mediaPlayer.seek(Duration.ZERO);
				// 미디어플레이어의 현재 위치를 처음으로 되돌리기
		}
	} );
    loadpage("/view/login/loginpane");
    }	// initialize END
    
    public void loadpage(String url) {
    	try {
    		Parent parent = FXMLLoader.load(getClass().getResource(url));
    		borderpane.setCenter(parent);
		} catch (Exception e) {System.out.println("Login loadpage Exception : "+e);}
    }
    
    

}	// c e
